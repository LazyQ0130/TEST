// Generate MBTI questions
let mbtiIdCounter = 1;
const generateMbtiQuestions = (countPerDim: number) => {
  const questions: Question[] = [];
  ['EI', 'SN', 'TF', 'JP'].forEach(dim => {
    const conceptList = MBTI_CONCEPTS[dim as keyof typeof MBTI_CONCEPTS];
    const target = conceptList.slice(0, countPerDim);
    target.forEach(([text, opt1, opt2]) => {
      questions.push({
        id: mbtiIdCounter++,
        text,
        category: dim,
        options: [
          { text: opt1.replace(/\s\([A-Z]\)$/, ''), value: opt1.match(/\(([A-Z])\)$/)?.[1] || dim[0] },
          { text: opt2.replace(/\s\([A-Z]\)$/, ''), value: opt2.match(/\(([A-Z])\)$/)?.[1] || dim[1] }
        ]
      });
    });
  });
  return questions;
};

